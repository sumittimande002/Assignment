
# ### 6. Square All Numbers Using map()

# **Question:**
# Given a list of numbers, use map() to return a list of squares.
numbers1 = [1, 2, 3, 4]
numbers2 = [5, 6, 7]
result = list(map(lambda l : l ** 2 ,numbers2 ))
print(result)

