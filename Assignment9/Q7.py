
# ### 7. Convert Strings to Uppercase Using map()

# **Question:**
# Convert a list of names to uppercase using map().
input1 = ["sairam", "anjali", "rahul"]
input2 = ["python", "django"]
upper_case = list(map(lambda l : l.upper() , input2))
print(upper_case)

# **Test Case 1:**
# Input:
# ["sairam", "anjali", "rahul"]

# Expected Output:
# ["SAIRAM", "ANJALI", "RAHUL"]

# **Test Case 2:**
# Input:
# ["python", "django"]

# Expected Output:
# ["PYTHON", "DJANGO"]

