# 6. Program to Convert Temperature from Celsius to Fahrenheit
# Problem Statement: Write a program to convert a temperature from Celsius to Fahrenheit.
# The program should take the temperature in Celsius as input and convert it into Fahrenheit.

temp_celsius = float(input("Enter a Temperature in Celsius : "))

temp_fah = 9/5 * temp_celsius + 32
print("Temperature in Fahrenheit : ",int(temp_fah))

