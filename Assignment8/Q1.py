# ### 1. Built-in Functions Practice

# **Question:**
# Write a program that takes a list of numbers and uses built-in functions to print

l = [10, 20, 5, 40]
# * Maximum value
print("Max : ",max(l))
# * Minimum value
print("Min : ",min(l))
# * Sum of elements
print("Sum : ",sum(l))
# * Length of the list
print("Len : ",len(l))
# **Test Case 1:**
# Input:
# [10, 20, 5, 40]

# Expected Output:
# Maximum: 40
# Minimum: 5
# Sum: 75
# Length: 4

# **Test Case 2:**
# Input:
# [3, 3, 3]

# Expected Output:
# Maximum: 3
# Minimum: 3
# Sum: 9
# Length: 3