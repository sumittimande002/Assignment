# 2.Write a program to compare two numbers using relational operators and display 
# the result.

a = int(input("Enter a Number : "))
b = int(input("Enter a Number : "))
print("----------------Relational operators -----------------")
print(" a == b : ", a==b)
print(" a >= b : ", a>=b)
print(" a > b : ", a>b)
print(" a <= b : ", a<=b)
print(" a < b :", a<b)
print(" a != b : ", a!=b)