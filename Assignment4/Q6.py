# 6.Write a program to check whether a given value exists in a list using 
# membership operators.
l = [10,50,60,20,40,30,70,90,55,22,44]
print(type(l))
print("------------- in -------------")
print(50 in l)
print(100 in l)
print("------------ not in ----------")
print(50 not in l)
print(100 not in l)