## 40. Program to Create Calculator using Functions

# **Problem Statement:** Write a program using functions to perform basic 
# arithmetic operations (addition, subtraction, multiplication, division).

def add(a,b):
    print("Add : ",a+b)

add(10,20)

def sub (a,b):
    print("Sub : ",a-b)
sub(20,10)

def mul(a,b):
    print("Mul : ",a*b)
mul(5,2)


def div(a,b):
    print("Div : ",a/b)
div(40,2)